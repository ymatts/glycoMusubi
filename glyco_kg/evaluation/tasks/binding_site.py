"""Glycosylation binding site prediction task using KG embeddings.

Position-wise prediction using site-level embeddings from the heterogeneous
graph.  Distinguishes N-linked (NXS/NXT sequon) vs O-linked (Ser/Thr) sites
when site type information is available.

Metrics
-------
- Residue-level AUC (target > 0.85)
- Site-level F1 (target > 0.70)
"""

from __future__ import annotations

import logging
from typing import Dict, List, Optional, Tuple

import numpy as np
import torch
from torch import Tensor
from torch_geometric.data import HeteroData

from glycoMusubi.evaluation.downstream import BaseDownstreamTask

logger = logging.getLogger(__name__)


class BindingSiteTask(BaseDownstreamTask):
    """Predict glycosylation binding sites on proteins.

    Positive sites are existing ``site`` nodes connected to proteins via
    ``has_site`` edges.  Negative sites are generated by sampling random
    protein positions that are **not** known glycosylation sites.

    Parameters
    ----------
    window_size : int
        Context window (unused when site embeddings are pre-computed,
        retained for compatibility with sequence-based extensions).
    classifier_hidden : int
        Hidden dimension for the two-layer MLP classifier.
    neg_ratio : int
        Number of negative samples per positive site.
    test_fraction : float
        Fraction of data reserved for testing.
    seed : int
        Random seed for reproducibility.
    """

    def __init__(
        self,
        window_size: int = 15,
        classifier_hidden: int = 128,
        neg_ratio: int = 3,
        test_fraction: float = 0.2,
        seed: int = 42,
    ) -> None:
        self.window_size = window_size
        self.classifier_hidden = classifier_hidden
        self.neg_ratio = neg_ratio
        self.test_fraction = test_fraction
        self.seed = seed

    @property
    def name(self) -> str:
        return "binding_site_prediction"

    # ------------------------------------------------------------------
    # Data preparation
    # ------------------------------------------------------------------

    def prepare_data(
        self,
        embeddings: Dict[str, Tensor],
        data: HeteroData,
    ) -> Tuple[Tensor, Tensor, Tensor, Tensor, Optional[np.ndarray]]:
        """Extract site nodes and build train/test features.

        Returns
        -------
        tuple of (X_train, y_train, X_test, y_test, site_types)
            ``site_types`` is a string array for the test set with values
            ``"N-linked"`` / ``"O-linked"`` / ``"unknown"``, or ``None``
            if type info is unavailable.
        """
        if "site" not in embeddings:
            raise ValueError(
                "Embeddings must contain a 'site' type for binding site "
                "prediction."
            )

        site_emb = embeddings["site"]
        n_sites = site_emb.shape[0]
        emb_dim = site_emb.shape[1]

        if n_sites == 0:
            raise ValueError("No site embeddings found.")

        # --- Retrieve protein association for context ---
        # Look for has_site edges: (protein, has_site, site) or
        # (enzyme, has_site, site)
        protein_site_key = None
        for candidate in [("protein", "has_site", "site"),
                          ("enzyme", "has_site", "site")]:
            if candidate in data.edge_types:
                protein_site_key = candidate
                break
            if (hasattr(data, "edge_index_dict")
                    and candidate in data.edge_index_dict):
                protein_site_key = candidate
                break

        # Build positive features from site embeddings
        pos_features = site_emb.detach().clone()
        num_pos = n_sites

        # Generate negative samples: random embeddings as surrogate
        # for non-glycosylation positions.  In a real dataset these would
        # come from non-annotated protein positions; here we sample from
        # the protein embedding space to create plausible negatives.
        rng = np.random.RandomState(self.seed)
        num_neg = num_pos * self.neg_ratio

        if "protein" in embeddings and embeddings["protein"].shape[0] > 0:
            prot_emb = embeddings["protein"]
            # Sample protein embeddings with added noise as surrogate
            # negatives (simulating non-glycosylation positions).
            sample_idx = torch.from_numpy(
                rng.randint(0, prot_emb.shape[0], size=num_neg)
            )
            noise = torch.randn(num_neg, prot_emb.shape[1]) * 0.1
            neg_raw = prot_emb[sample_idx] + noise
            # Pad or truncate to match site embedding dim
            if neg_raw.shape[1] < emb_dim:
                neg_features = torch.nn.functional.pad(
                    neg_raw, (0, emb_dim - neg_raw.shape[1])
                )
            elif neg_raw.shape[1] > emb_dim:
                neg_features = neg_raw[:, :emb_dim]
            else:
                neg_features = neg_raw
        else:
            # Fallback: random noise negatives
            neg_features = torch.randn(num_neg, emb_dim) * 0.1

        all_features = torch.cat([pos_features, neg_features.detach()], dim=0)
        all_labels = torch.cat([
            torch.ones(num_pos, dtype=torch.float32),
            torch.zeros(num_neg, dtype=torch.float32),
        ])

        # Random split
        n_total = all_features.shape[0]
        indices = np.arange(n_total)
        rng.shuffle(indices)
        split = int(n_total * (1 - self.test_fraction))
        train_idx = indices[:split]
        test_idx = indices[split:]

        X_train = all_features[train_idx]
        y_train = all_labels[train_idx]
        X_test = all_features[test_idx]
        y_test = all_labels[test_idx]

        # Extract site type info if available
        site_types = self._extract_site_types(data, test_idx, num_pos)

        return X_train, y_train, X_test, y_test, site_types

    # ------------------------------------------------------------------
    # Evaluation
    # ------------------------------------------------------------------

    def evaluate(
        self,
        embeddings: Dict[str, Tensor],
        data: HeteroData,
        **kwargs,
    ) -> Dict[str, float]:
        """Train site classifier and compute evaluation metrics.

        Returns
        -------
        dict[str, float]
            Keys: ``residue_auc``, ``site_f1``, and optionally
            ``n_linked_auc``, ``o_linked_auc`` when type info is present.
        """
        from sklearn.metrics import f1_score, roc_auc_score

        train_data = kwargs.get("train_data")
        test_data = kwargs.get("test_data")
        if train_data is not None and test_data is not None:
            X_train, y_train, X_test, y_test, site_types = self._prepare_data_from_external_split(
                embeddings=embeddings,
                full_data=data,
                train_data=train_data,
                test_data=test_data,
                positive_site_indices=kwargs.get("positive_site_indices"),
                split_seed=kwargs.get("split_seed"),
            )
        else:
            X_train, y_train, X_test, y_test, site_types = self.prepare_data(
                embeddings, data
            )

        # Train MLP
        model = self._build_classifier(X_train.shape[1])
        self._train_classifier(model, X_train, y_train)

        # Predict
        model.eval()
        with torch.no_grad():
            logits = model(X_test).squeeze(-1)
            y_scores = torch.sigmoid(logits).cpu().numpy()

        y_true = y_test.cpu().numpy()
        y_pred = (y_scores >= 0.5).astype(np.float32)

        results: Dict[str, float] = {}

        # Residue-level AUC
        if len(np.unique(y_true)) > 1:
            results["residue_auc"] = float(roc_auc_score(y_true, y_scores))
        else:
            results["residue_auc"] = 0.0
            logger.warning("Only one class in test set; residue AUC set to 0.0.")

        # Site-level F1
        results["site_f1"] = float(f1_score(y_true, y_pred, zero_division=0.0))
        for k, v in getattr(self, "_external_split_counts", {}).items():
            results[k] = float(v)

        # Per-type AUC if site type info available
        if site_types is not None:
            for stype in ("N-linked", "O-linked"):
                mask = site_types == stype
                if mask.sum() > 0 and len(np.unique(y_true[mask])) > 1:
                    results[f"{stype.lower().replace('-', '_')}_auc"] = float(
                        roc_auc_score(y_true[mask], y_scores[mask])
                    )

        logger.info(
            "BindingSite: residue_AUC=%.4f, site_F1=%.4f",
            results["residue_auc"],
            results["site_f1"],
        )

        return results

    def _prepare_data_from_external_split(
        self,
        embeddings: Dict[str, Tensor],
        full_data: HeteroData,
        train_data: HeteroData,
        test_data: HeteroData,
        positive_site_indices: Optional[List[int]] = None,
        split_seed: Optional[int] = None,
    ) -> Tuple[Tensor, Tensor, Tensor, Tensor, Optional[np.ndarray]]:
        if "site" not in embeddings:
            raise ValueError("Embeddings must contain 'site'.")
        site_emb = embeddings["site"]
        n_sites = int(site_emb.shape[0])
        if n_sites == 0:
            raise ValueError("No site embeddings found.")

        edge_key = None
        for candidate in [("protein", "has_site", "site"), ("enzyme", "has_site", "site")]:
            if candidate in full_data.edge_types and candidate in train_data.edge_types and candidate in test_data.edge_types:
                edge_key = candidate
                break
        if edge_key is None:
            raise ValueError("No has_site edge type found in split data.")

        full_ei = full_data[edge_key].edge_index
        # Identify glycosylation-positive sites. Prefer explicit list from run_downstream.
        if positive_site_indices is not None and len(positive_site_indices) > 0:
            pos_site_set = set(int(x) for x in positive_site_indices)
        else:
            pos_site_set = set(torch.unique(full_ei[1]).tolist())

        # Build protein->sites map and positive pairs.
        protein_to_sites: Dict[int, List[int]] = {}
        pos_pairs: List[Tuple[int, int]] = []
        for i in range(full_ei.size(1)):
            p = int(full_ei[0, i].item())
            s = int(full_ei[1, i].item())
            protein_to_sites.setdefault(p, []).append(s)
            if s in pos_site_set:
                pos_pairs.append((p, s))
        if len(pos_pairs) == 0:
            raise ValueError("No positive glycosylation sites available for binding-site evaluation.")

        # Protein-level holdout split.
        proteins = sorted({p for p, _ in pos_pairs})
        rng = np.random.RandomState(self.seed if split_seed is None else int(split_seed))
        rng.shuffle(proteins)
        split = int(len(proteins) * (1 - self.test_fraction))
        train_proteins = set(proteins[:split])
        test_proteins = set(proteins[split:])
        if len(train_proteins) == 0 or len(test_proteins) == 0:
            raise ValueError("Protein-level split failed (empty train/test proteins).")

        train_pos_sites = [s for p, s in pos_pairs if p in train_proteins]
        test_pos_sites = [s for p, s in pos_pairs if p in test_proteins]
        train_sites = torch.tensor(sorted(set(train_pos_sites)), dtype=torch.long)
        test_sites = torch.tensor(sorted(set(test_pos_sites)), dtype=torch.long)
        if train_sites.numel() == 0 or test_sites.numel() == 0:
            raise ValueError("Train/test split has no positive glycosylation site nodes.")

        # Same-protein negatives: choose non-glycosylation sites linked to the same protein.
        global_non_pos = [i for i in range(n_sites) if i not in pos_site_set]
        if len(global_non_pos) == 0:
            raise ValueError("No non-glycosylation site candidates available.")

        def _sample_neg_for_protein(protein_set: set[int], num_needed: int) -> torch.Tensor:
            neg: List[int] = []
            attempts = 0
            max_attempts = max(10 * num_needed, 2000)
            p_list = list(protein_set)
            while len(neg) < num_needed and attempts < max_attempts:
                p = int(rng.choice(p_list))
                cands = [s for s in protein_to_sites.get(p, []) if s not in pos_site_set]
                if cands:
                    neg.append(int(rng.choice(cands)))
                else:
                    neg.append(int(rng.choice(global_non_pos)))
                attempts += 1
            if len(neg) == 0:
                raise ValueError("Failed to sample binding-site negatives.")
            return torch.tensor(neg, dtype=torch.long)

        tr_neg = _sample_neg_for_protein(train_proteins, int(train_sites.numel()) * self.neg_ratio)
        te_neg = _sample_neg_for_protein(test_proteins, int(test_sites.numel()) * self.neg_ratio)

        X_train = torch.cat([site_emb[train_sites], site_emb[tr_neg]], dim=0)
        y_train = torch.cat(
            [
                torch.ones(train_sites.numel(), dtype=torch.float32),
                torch.zeros(tr_neg.numel(), dtype=torch.float32),
            ],
            dim=0,
        )
        X_test = torch.cat([site_emb[test_sites], site_emb[te_neg]], dim=0)
        y_test = torch.cat(
            [
                torch.ones(test_sites.numel(), dtype=torch.float32),
                torch.zeros(te_neg.numel(), dtype=torch.float32),
            ],
            dim=0,
        )

        site_types = self._extract_site_types_for_indices(full_data, test_sites.tolist(), int(te_neg.numel()))
        self._external_split_counts = {
            "n_train_pos": float(train_sites.numel()),
            "n_train_neg": float(tr_neg.numel()),
            "n_test_pos": float(test_sites.numel()),
            "n_test_neg": float(te_neg.numel()),
            "n_train_proteins": float(len(train_proteins)),
            "n_test_proteins": float(len(test_proteins)),
            "split_seed": float(split_seed) if split_seed is not None else -1.0,
        }
        return X_train, y_train, X_test, y_test, site_types

    def _extract_site_types_for_indices(
        self,
        data: HeteroData,
        pos_site_indices: List[int],
        num_neg: int,
    ) -> Optional[np.ndarray]:
        site_type_attr = None
        if "site" in data.node_types:
            site_store = data["site"]
            for attr_name in ("glyco_type", "site_type"):
                if hasattr(site_store, attr_name):
                    site_type_attr = getattr(site_store, attr_name)
                    break
        if site_type_attr is None:
            return None

        types: List[str] = []
        for idx in pos_site_indices:
            raw = site_type_attr[idx]
            if isinstance(raw, (bytes, str)):
                raw_str = raw.decode() if isinstance(raw, bytes) else raw
            elif hasattr(raw, "item"):
                raw_str = str(raw.item())
            else:
                raw_str = str(raw)
            raw_lower = raw_str.lower()
            if "n-linked" in raw_lower or "n_linked" in raw_lower:
                types.append("N-linked")
            elif "o-linked" in raw_lower or "o_linked" in raw_lower:
                types.append("O-linked")
            else:
                types.append("unknown")
        types.extend(["unknown"] * num_neg)
        return np.array(types, dtype=object)

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _build_classifier(self, input_dim: int) -> torch.nn.Module:
        """Build a two-layer MLP binary classifier."""
        return torch.nn.Sequential(
            torch.nn.Linear(input_dim, self.classifier_hidden),
            torch.nn.ReLU(),
            torch.nn.Dropout(0.3),
            torch.nn.Linear(self.classifier_hidden, 1),
        )

    def _train_classifier(
        self,
        model: torch.nn.Module,
        X: Tensor,
        y: Tensor,
        epochs: int = 100,
        lr: float = 1e-3,
    ) -> None:
        """Train the MLP with BCE loss."""
        optimizer = torch.optim.Adam(model.parameters(), lr=lr)
        loss_fn = torch.nn.BCEWithLogitsLoss()
        model.train()
        for _ in range(epochs):
            optimizer.zero_grad()
            logits = model(X).squeeze(-1)
            loss = loss_fn(logits, y)
            loss.backward()
            optimizer.step()

    @staticmethod
    def _extract_site_types(
        data: HeteroData,
        test_idx: np.ndarray,
        num_pos: int,
    ) -> Optional[np.ndarray]:
        """Extract N-linked / O-linked type labels for test samples.

        Uses the ``glyco_type`` or ``site_type`` metadata on site nodes
        when available.  Returns ``None`` if no type info exists.
        """
        site_type_attr = None
        if hasattr(data, "site") or "site" in data.node_types:
            site_store = data["site"]
            for attr_name in ("glyco_type", "site_type"):
                if hasattr(site_store, attr_name):
                    site_type_attr = getattr(site_store, attr_name)
                    break

        if site_type_attr is None:
            return None

        # Map type info to test indices
        n_test = len(test_idx)
        types = np.array(["unknown"] * n_test, dtype=object)

        for i, idx in enumerate(test_idx):
            if idx < num_pos:
                # This is a positive sample — look up site type
                raw = site_type_attr[idx]
                if isinstance(raw, (bytes, str)):
                    raw_str = raw.decode() if isinstance(raw, bytes) else raw
                elif hasattr(raw, "item"):
                    raw_str = str(raw.item())
                else:
                    raw_str = str(raw)

                raw_lower = raw_str.lower()
                if "n-linked" in raw_lower or "n_linked" in raw_lower:
                    types[i] = "N-linked"
                elif "o-linked" in raw_lower or "o_linked" in raw_lower:
                    types[i] = "O-linked"
            # Negatives stay "unknown"

        return types
